# ¬´f¬ª. –ï—Å–ª–∏ –±—É–∫–≤–∞ ¬´f¬ª –≤—Å—Ç—Ä–µ—á–∞–µ—Ç—Å—è —Ç–æ–ª—å–∫–æ –æ–¥–∏–Ω —Ä–∞–∑, –≤—ã–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–æ ¬´-1¬ª (–±–µ–∑ –∫–∞–≤—ã—á–µ–∫), –∞ –µ—Å–ª–∏ –Ω–µ –≤—Å—Ç—Ä–µ—á–∞–µ—Ç—Å—è –Ω–∏ 
# —Ä–∞–∑—É ‚Äì –≤—ã–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–æ ¬´-2¬ª (–±–µ–∑ –∫–∞–≤—ã—á–µ–∫).

str_one = input()

if str_one.count('f') == 0:
    print(-2)
elif str_one.count('f') == 1:
    print(-1)
elif str_one.count('f') > 1:
    print(str_one.find('f', str_one.find('f')+1))



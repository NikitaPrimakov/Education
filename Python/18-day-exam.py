# –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞—ë—Ç—Å—è —Å—Ç—Ä–æ–∫–∞ —Ç–µ–∫—Å—Ç–∞. –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è —É–¥–∞–ª—è–µ—Ç –∏–∑ –Ω–µ–µ –≤—Å–µ —Å–∏–º–≤–æ–ª—ã —Å –∏–Ω–¥–µ–∫—Å–∞–º–∏, 
# –∫—Ä–∞—Ç–Ω—ã–º–∏ 3, —Ç–æ –µ—Å—Ç—å —Å–∏–º–≤–æ–ª—ã —Å –∏–Ω–¥–µ–∫—Å–∞–º–∏ 0, 3, 6, ....

str_one = input("Enter your string: ")
result = ""

for i in range(len(str_one)):
    if i % 3 != 0:
        result += str_one[i]

print(result)





# –¥–∞–Ω–Ω—É—é —Å—Ç—Ä–æ–∫—É –≥–µ–Ω–µ—Ç–∏—á–µ—Å–∫–æ–≥–æ –∫–æ–¥–∞.

str_one = input("Enter your string: ")

count_a = 0
count_g = 0
count_c = 0
count_t = 0

for i in range(len(str_one)):

    if  str_one[i] == '–ê' or str_one[i] == '–∞':
        count_a += 1
        
    if str_one[i] == '–ì'or str_one[i] == '–≥':
        count_g += 1
        
    if str_one[i] == '–¶'or str_one[i] == '—Ü':
        count_c += 1

    if str_one[i] == '–¢'or str_one[i] == '—Ç':
        count_t += 1

print("–ê–¥–µ–Ω–∏–Ω:", count_a)
print("–ì—É–∞–Ω–∏–Ω:", count_g)
print("–¶–∏—Ç–æ–∑–∏–Ω:", count_c)
print("–¢–∏–º–∏–Ω:", count_t)



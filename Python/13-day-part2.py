# Задача 4.

# В 1769 году Леонард Эйлер сформулировал обобщенную версию Великой теоремы Ферма, предполагая, что по крайней 
# мере n энных степеней необходимо для получения суммы, которая сама является энной степенью для n>2. 
# Напишите программу для опровержения гипотезы Эйлера (продержавшейся до 1967 года) и найдите четыре 
# положительных целых числа, сумма 5-х степеней которых равна 5-й степени другого положительного целого числа. 
# Таким образом, найдите пять натуральных чисел a,b,c,d,e, удовлетворяющих условию: a^5 + b^5 + c^5 +d^5 = e^5
# В ответе укажите сумму a+b+c+d+e.


max_limit = 150  # Ограничиваем диапазон поиска
for a in range(1, max_limit):
    for b in range(a, max_limit):
        for c in range(b, max_limit):
            for d in range(c, max_limit):
                sum_powers = a**5 + b**5 + c**5 + d**5
                e = round(sum_powers ** (1/5))
                # Проверяем, что e^5 равно сумме (с учетом возможных ошибок округления)
                if e**5 == sum_powers and e != 0:
                    print(a + b + c + d + e)
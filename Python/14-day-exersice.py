# Цифра 1

# На вход программе подаётся одна строка состоящая из цифр. Напишите программу, которая считает сумму цифр 
# данной строки.

strOne = input("Введите строку состоящую из чисел: ")

summ = 0

for i in range(len(strOne)):

    num = int(strOne[i])
    summ += num

print(summ)


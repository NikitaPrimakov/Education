# Упражнение 6

# На вход программе подаётся одна строка. Напишите программу, которая определяет, сколько раз в строке 
# встречаются символы + и *, и выводит текст в следующем формате:

# Символ + встречается <n> раз
# Символ * встречается <m> раз
# где <n>, <m> – количество вхождений символов + и * в строку соответственно.


strOne = input("Enter your string: ")

n = 0
m = 0

for i in range(len(strOne)):

    if strOne[i] == '+':
        n += 1
    if strOne[i] == '*':
        m += 1

print(f"Символ + встречается {n} раз")
print(f"Символ * встречается {m} раз")


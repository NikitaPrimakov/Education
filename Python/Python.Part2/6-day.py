for r in range(rows):
    for c in range(cols):
        print(matrix[r][c], end=' ')
    print()

                  
#  –†–µ–∑—É–ª—å—Ç–∞—Ç–æ–º —Ä–∞–±–æ—Ç—ã —Ç–∞–∫–æ–≥–æ –∫–æ–¥–∞ –±—É–¥–µ—Ç:

# 277 -930 11 0 
# 9 43 6 87 
# 4456 8 290 7 

                  
# –í—ã–≤–µ–¥–µ–Ω–Ω–∞—è –º–∞—Ç—Ä–∏—Ü–∞ –Ω–µ —Å–∏–ª—å–Ω–æ –ø–æ—Ö–æ–∂–∞ –Ω–∞ —É–ø–æ—Ä—è–¥–æ—á–µ–Ω–Ω—ã–π –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫. –≠–ª–µ–º–µ–Ω—Ç—ã –º–∞—Ç—Ä–∏—Ü—ã –∏–º–µ—é—Ç —Ä–∞–∑–Ω–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Ä–∞–∑—Ä—è–¥–æ–≤ –∏ 
# —Ä–µ–∑—É–ª—å—Ç–∞—Ç –≤—ã–≤–æ–¥–∞ –ø–æ–ª—É—á–∞–µ—Ç—Å—è —Å–º–∞–∑–∞–Ω–Ω—ã–º. –î–ª—è —Ä–µ—à–µ–Ω–∏—è –ø—Ä–æ–±–ª–µ–º—ã —É–¥–æ–±–Ω–æ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å —Å—Ç—Ä–æ–∫–æ–≤—ã–µ –º–µ—Ç–æ–¥—ã ljust() –∏ rjust().


## –ú–µ—Ç–æ–¥ ljust()
#----------------

# –°—Ç—Ä–æ–∫–æ–≤—ã–π –º–µ—Ç–æ–¥ ljust() –≤—ã—Ä–∞–≤–Ω–∏–≤–∞–µ—Ç —Ç–µ–∫—Å—Ç –ø–æ —à–∏—Ä–∏–Ω–µ, –¥–æ–±–∞–≤–ª—è—è –ø—Ä–æ–±–µ–ª—ã –≤ –∫–æ–Ω–µ—Ü —Ç–µ–∫—Å—Ç–∞.

# –†–µ–∑—É–ª—å—Ç–∞—Ç–æ–º –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è —Å–ª–µ–¥—É—é—â–µ–≥–æ –∫–æ–¥–∞:


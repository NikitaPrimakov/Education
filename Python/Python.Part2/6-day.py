# –†–µ–∑—É–ª—å—Ç–∞—Ç–æ–º –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è —Å–ª–µ–¥—É—é—â–µ–≥–æ –∫–æ–¥–∞:

print('abcdefg'.ljust(3))

# –±—É–¥–µ—Ç: abcdefg

                  
# –°—Ç—Ä–æ–∫–æ–≤—ã–π –º–µ—Ç–æ–¥ ljust() –∏—Å–ø–æ–ª—å–∑—É–µ—Ç –≤–º–µ—Å—Ç–æ –ø—Ä–æ–±–µ–ª–∞ –¥—Ä—É–≥–æ–π —Å–∏–º–≤–æ–ª, –µ—Å–ª–∏ –ø–µ—Ä–µ–¥–∞—Ç—å –µ–º—É –≤—Ç–æ—Ä–æ–π –∞—Ä–≥—É–º–µ–Ω—Ç, –Ω–µ–æ–±—è–∑–∞—Ç–µ–ª—å–Ω—ã–π.

# –†–µ–∑—É–ª—å—Ç–∞—Ç–æ–º –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è —Å–ª–µ–¥—É—é—â–µ–≥–æ –∫–æ–¥–∞:

print('a'.ljust(5, '*'))
print('ab'.ljust(5, '$'))
print('abc'.ljust(5, '#'))

                  
# –±—É–¥–µ—Ç:

# a****
# ab$$$
# abc##

## –ú–µ—Ç–æ–¥ rjust()
#---------------

# –°—Ç—Ä–æ–∫–æ–≤—ã–π –º–µ—Ç–æ–¥ rjust() –≤—ã—Ä–∞–≤–Ω–∏–≤–∞–µ—Ç —Ç–µ–∫—Å—Ç –ø–æ —à–∏—Ä–∏–Ω–µ, –¥–æ–±–∞–≤–ª—è—è –ø—Ä–æ–±–µ–ª—ã –≤ –Ω–∞—á–∞–ª–æ —Ç–µ–∫—Å—Ç–∞.

# –†–µ–∑—É–ª—å—Ç–∞—Ç–æ–º –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è —Å–ª–µ–¥—É—é—â–µ–≥–æ –∫–æ–¥–∞:

print('a'.rjust(3))
print('ab'.rjust(3))
print('abc'.rjust(3))

                  
# –±—É–¥–µ—Ç:
# ‚éµ‚éµa
# ‚éµab
# abc

                  
# –ò—Å—Ö–æ–¥–Ω–∞—è —Å—Ç—Ä–æ–∫–∞ –Ω–µ –æ–±—Ä–µ–∑–∞–µ—Ç—Å—è, –¥–∞–∂–µ –µ—Å–ª–∏ –≤ –Ω–µ–π –±–æ–ª—å—à–µ —Å–∏–º–≤–æ–ª–æ–≤, —á–µ–º –Ω—É–∂–Ω–æ.

# –†–µ–∑—É–ª—å—Ç–∞—Ç–æ–º –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è —Å–ª–µ–¥—É—é—â–µ–≥–æ –∫–æ–¥–∞:

print('abcdefg'.rjust(3))

                  
# –±—É–¥–µ—Ç: abcdefg

                  
# –°—Ç—Ä–æ–∫–æ–≤—ã–π –º–µ—Ç–æ–¥ rjust() –∏—Å–ø–æ–ª—å–∑—É–µ—Ç –≤–º–µ—Å—Ç–æ –ø—Ä–æ–±–µ–ª–∞ –¥—Ä—É–≥–æ–π —Å–∏–º–≤–æ–ª, –µ—Å–ª–∏ –ø–µ—Ä–µ–¥–∞—Ç—å –µ–º—É –≤—Ç–æ—Ä–æ–π –∞—Ä–≥—É–º–µ–Ω—Ç, –Ω–µ–æ–±—è–∑–∞—Ç–µ–ª—å–Ω—ã–π.

# –†–µ–∑—É–ª—å—Ç–∞—Ç–æ–º –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è —Å–ª–µ–¥—É—é—â–µ–≥–æ –∫–æ–¥–∞:

print('a'.rjust(5, '*'))
print('ab'.rjust(5, '$'))
print('abc'.rjust(5, '#'))

# –ü—Ä–∏–º–µ–Ω–∏–≤ –º–µ—Ç–æ–¥ ljust() –¥–ª—è –≤—ã—Ä–∞–≤–Ω–∏–≤–∞–Ω–∏—è —Å—Ç–æ–ª–±—Ü–æ–≤, –ø—Ä–∏ –≤—ã–≤–æ–¥–µ —Ç–∞–±–ª–∏—Ü—ã –º—ã –ø–æ–ª—É—á–∏–º —Å–ª–µ–¥—É—é—â–∏–π –∫–æ–¥:

rows, cols = 3, 4  # rows - –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å—Ç—Ä–æ–∫, cols - –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å—Ç–æ–ª–±—Ü–æ–≤

matrix  = [[277, -930, 11, 0],
           [9, 43, 6, 87],
           [4456, 8, 290, 7]]

for r in range(rows):
    for c in range(cols):
        print(str(matrix[r][c]).ljust(6), end='')
    print()

                  
# –†–µ–∑—É–ª—å—Ç–∞—Ç–æ–º –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è —Ç–∞–∫–æ–≥–æ –∫–æ–¥–∞ –±—É–¥–µ—Ç:

# 277   -930  11    0     
# 9     43    6     87    
# 4456  8     290   7     


## –ö–≤–∞–¥—Ä–∞—Ç–Ω—ã–µ –º–∞—Ç—Ä–∏—Ü—ã
#--------------------

# –ú–∞—Ç—Ä–∏—Ü–∞ —Å –æ–¥–∏–Ω–∞–∫–æ–≤—ã–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ–º —Å—Ç—Ä–æ–∫ –∏ —Å—Ç–æ–ª–±—Ü–æ–≤ –Ω–∞–∑—ã–≤–∞–µ—Ç—Å—è –∫–≤–∞–¥—Ä–∞—Ç–Ω–æ–π. –£ –∫–≤–∞–¥—Ä–∞—Ç–Ω–æ–π –º–∞—Ç—Ä–∏—Ü—ã –µ—Å—Ç—å –¥–≤–µ –¥–∏–∞–≥–æ–Ω–∞–ª–∏:

# –≥–ª–∞–≤–Ω–∞—è: –ø—Ä–æ—Ö–æ–¥–∏—Ç –∏–∑ –≤–µ—Ä—Ö–Ω–µ–≥–æ –ª–µ–≤–æ–≥–æ –≤ –ø—Ä–∞–≤—ã–π –Ω–∏–∂–Ω–∏–π —É–≥–æ–ª –º–∞—Ç—Ä–∏—Ü—ã;
# –ø–æ–±–æ—á–Ω–∞—è: –ø—Ä–æ—Ö–æ–¥–∏—Ç –∏–∑ –Ω–∏–∂–Ω–µ–≥–æ –ª–µ–≤–æ–≥–æ –≤ –ø—Ä–∞–≤—ã–π –≤–µ—Ä—Ö–Ω–∏–π —É–≥–æ–ª –º–∞—Ç—Ä–∏—Ü—ã.


# –≠–ª–µ–º–µ–Ω—Ç—ã —Å —Ä–∞–≤–Ω—ã–º–∏ –∏–Ω–¥–µ–∫—Å–∞–º–∏ i == j –Ω–∞—Ö–æ–¥—è—Ç—Å—è –Ω–∞ –≥–ª–∞–≤–Ω–æ–π –¥–∏–∞–≥–æ–Ω–∞–ª–∏. –¢–∞–∫–∏–µ —ç–ª–µ–º–µ–Ω—Ç—ã –æ–±–æ–∑–Ω–∞—á–∞—é—Ç—Å—è matrix[i][i].

# –≠–ª–µ–º–µ–Ω—Ç—ã —Å –∏–Ω–¥–µ–∫—Å–∞–º–∏ i –∏ j, —Å–≤—è–∑–∞–Ω–Ω—ã–º–∏ —Å–æ–æ—Ç–Ω–æ—à–µ–Ω–∏–µ–º i + j + 1 = n (–∏–ª–∏ j = n - i - 1), –≥–¥–µ n ‚Äî —Ä–∞–∑–º–µ—Ä–Ω–æ—Å—Ç—å –º–∞—Ç—Ä–∏—Ü—ã, 
# –Ω–∞—Ö–æ–¥—è—Ç—Å—è –Ω–∞ –ø–æ–±–æ—á–Ω–æ–π –¥–∏–∞–≥–æ–Ω–∞–ª–∏.

# –¢–∞–∫–∏–º –æ–±—Ä–∞–∑–æ–º, —á—Ç–æ–±—ã —É—Å—Ç–∞–Ω–æ–≤–∏—Ç—å —ç–ª–µ–º–µ–Ω—Ç—ã –≥–ª–∞–≤–Ω–æ–π –∏–ª–∏ –ø–æ–±–æ—á–Ω–æ–π –¥–∏–∞–≥–æ–Ω–∞–ª–∏, –¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ –æ–¥–Ω–æ–≥–æ —Ü–∏–∫–ª–∞.


n = 8
matrix = [[0]*n for _ in range(n)]    # —Å–æ–∑–¥–∞–µ–º –∫–≤–∞–¥—Ä–∞—Ç–Ω—É—é –º–∞—Ç—Ä–∏—Ü—É —Ä–∞–∑–º–µ—Ä–æ–º 8√ó8

for i in range(n):                    # –∑–∞–ø–æ–ª–Ω—è–µ–º –≥–ª–∞–≤–Ω—É—é –¥–∏–∞–≥–æ–Ω–∞–ª—å –µ–¥–∏–Ω–∏—Ü–∞–º–∏, –∞ –ø–æ–±–æ—á–Ω—É—é –¥–≤–æ–π–∫–∞–º–∏
    matrix[i][i] = 1
    matrix[i][n-i-1] = 2

for r in range(n):                    # –≤—ã–≤–æ–¥–∏–º –º–∞—Ç—Ä–∏—Ü—É
    for c in range(n):
        print(matrix[r][c], end=' ')
    print()

# ! –ò–Ω–¥–µ–∫—Å—ã i –∏ j —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –Ω–∞ –≥–ª–∞–≤–Ω–æ–π –¥–∏–∞–≥–æ–Ω–∞–ª–∏ —Å–≤—è–∑–∞–Ω—ã —Å–æ–æ—Ç–Ω–æ—à–µ–Ω–∏–µ–º i = j. –ò–Ω–¥–µ–∫—Å—ã i –∏ j —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –Ω–∞ –ø–æ–±–æ—á–Ω–æ–π –¥–∏–∞–≥–æ–Ω–∞–ª–∏ —Å–≤—è–∑–∞–Ω—ã 
# —Å–æ–æ—Ç–Ω–æ—à–µ–Ω–∏–µ–º i + j + 1 = n (–∏–ª–∏  j = n - i - 1), –≥–¥–µ n ‚Äî —Ä–∞–∑–º–µ—Ä–Ω–æ—Å—Ç—å –º–∞—Ç—Ä–∏—Ü—ã.


# –ß–∞—Å—Ç–Ω—ã–π —Å–ª—É—á–∞–π –≤–ª–æ–∂–µ–Ω–Ω—ã—Ö —Å–ø–∏—Å–∫–æ–≤ ‚Äî –º–∞—Ç—Ä–∏—Ü—ã. –≠—Ç–æ –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω—ã–µ —Ç–∞–±–ª–∏—Ü—ã, –∑–∞–ø–æ–ª–Ω–µ–Ω–Ω—ã–µ –∫–∞–∫–∏–º–∏-—Ç–æ –∑–Ω–∞—á–µ–Ω–∏—è–º–∏, –æ–±—ã—á–Ω–æ —á–∏—Å–ª–∞–º–∏.

# –î–ª—è —Ä–∞–±–æ—Ç—ã —Å –º–∞—Ç—Ä–∏—Ü–∞–º–∏ –Ω—É–∂–Ω–æ —É–º–µ—Ç—å –ø–æ–ª—É—á–∞—Ç—å —ç–ª–µ–º–µ–Ω—Ç i-–π —Å—Ç—Ä–æ–∫–∏ j-–≥–æ —Å—Ç–æ–ª–±—Ü–∞. 
# –î–ª—è —ç—Ç–æ–≥–æ –æ–±—ã—á–Ω–æ –∑–∞–≤–æ–¥—è—Ç —Å–ø–∏—Å–æ–∫ —Å—Ç—Ä–æ–∫ –º–∞—Ç—Ä–∏—Ü—ã, –≥–¥–µ –∫–∞–∂–¥–∞—è —Å—Ç—Ä–æ–∫–∞ ‚Äî —Å–ø–∏—Å–æ–∫ —ç–ª–µ–º–µ–Ω—Ç–æ–≤. –ü–æ–ª—É—á–∞–µ—Ç—Å—è –≤–ª–æ–∂–µ–Ω–Ω—ã–π —Å–ø–∏—Å–æ–∫ –∏–ª–∏ —Å–ø–∏—Å–æ–∫ —Å–ø–∏—Å–∫–æ–≤. 
# –¢–µ–ø–µ—Ä—å, —á—Ç–æ–±—ã –ø–æ–ª—É—á–∏—Ç—å –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–Ω—ã–π —ç–ª–µ–º–µ–Ω—Ç, –¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ –∏–∑ —Å–ø–∏—Å–∫–∞ —Å—Ç—Ä–æ–∫ –º–∞—Ç—Ä–∏—Ü—ã –≤—ã–±—Ä–∞—Ç—å i-—é –∏ –≤–∑—è—Ç—å j-–π —ç–ª–µ–º–µ–Ω—Ç —ç—Ç–æ–π —Å—Ç—Ä–æ–∫–∏.

# –î–∞–≤–∞–π—Ç–µ –∑–∞–≤–µ–¥–µ–º –º–∞—Ç—Ä–∏—Ü—É —Ä–∞–∑–º–µ—Ä–∞ 3√ó4 (3 —Å—Ç—Ä–æ–∫–∏ –∏ 4 —Å—Ç–æ–ª–±—Ü–∞), —Å–æ–¥–µ—Ä–∂–∞—â—É—é —á–∏—Å–ª–∞, –∏ –ø–æ–ª—É—á–∏–º —ç–ª–µ–º–µ–Ω—Ç –Ω–∞ –ø–æ–∑–∏—Ü–∏–∏ (2, 3), 
# —Ç–æ –µ—Å—Ç—å —ç–ª–µ–º–µ–Ω—Ç –≤—Ç–æ—Ä–æ–π —Å—Ç—Ä–æ–∫–∏ –≤ —Ç—Ä–µ—Ç—å–µ–º —Å—Ç–æ–ª–±—Ü–µ.

matrix  = [[2, -5, -11, 0],
           [-9, 4, 6, 13],
           [4, 7, 12, -2]]

print(matrix[1][2])

# –í –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π matrix —Ö—Ä–∞–Ω–∏—Ç—Å—è –≤—Å—è –º–∞—Ç—Ä–∏—Ü–∞, –ø—Ä–∏ —ç—Ç–æ–º matrix[1] ‚Äî —Å–ø–∏—Å–æ–∫ –∑–Ω–∞—á–µ–Ω–∏–π –≤–æ –≤—Ç–æ—Ä–æ–π —Å—Ç—Ä–æ–∫–µ, 
# matrix[1][2] ‚Äî —ç–ª–µ–º–µ–Ω—Ç –≤ —Ç—Ä–µ—Ç—å–µ–º —Å—Ç–æ–ª–±—Ü–µ —ç—Ç–æ–π —Å—Ç—Ä–æ–∫–∏.

## –ü–µ—Ä–µ–±–æ—Ä —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –º–∞—Ç—Ä–∏—Ü—ã
#--------------------------

# –ß—Ç–æ–±—ã –ø–µ—Ä–µ–±—Ä–∞—Ç—å —ç–ª–µ–º–µ–Ω—Ç—ã –º–∞—Ç—Ä–∏—Ü—ã, –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –≤–ª–æ–∂–µ–Ω–Ω—ã–µ —Ü–∏–∫–ª—ã. –ù–∞–ø—Ä–∏–º–µ—Ä, –≤—ã–≤–µ–¥–µ–º –Ω–∞ —ç–∫—Ä–∞–Ω –≤—Å–µ —ç–ª–µ–º–µ–Ω—Ç—ã –º–∞—Ç—Ä–∏—Ü—ã, 
# –ø–µ—Ä–µ–±–∏—Ä–∞—è –∏—Ö –ø–æ —Å—Ç—Ä–æ–∫–∞–º:

rows, cols = 3, 4  # rows - –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å—Ç—Ä–æ–∫, cols - –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å—Ç–æ–ª–±—Ü–æ–≤


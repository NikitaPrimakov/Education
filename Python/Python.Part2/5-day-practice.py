num = int(input('Enter your number "n": '))

# –°–æ–∑–¥–∞–µ–º —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫ –ü–∞—Å–∫–∞–ª—è
triangle = []

for i in range(num):
    # –°–æ–∑–¥–∞–µ–º –Ω–æ–≤—É—é —Å—Ç—Ä–æ–∫—É
    row = []
    
    # –ö–∞–∂–¥–∞—è —Å—Ç—Ä–æ–∫–∞ –∏–º–µ–µ—Ç i+1 —ç–ª–µ–º–µ–Ω—Ç–æ–≤
    for j in range(i + 1):
        if j == 0 or j == i:
            # –ü–µ—Ä–≤—ã–π –∏ –ø–æ—Å–ª–µ–¥–Ω–∏–π —ç–ª–µ–º–µ–Ω—Ç—ã –≤—Å–µ–≥–¥–∞ —Ä–∞–≤–Ω—ã 1
            row.append(1)
        else:
            # –û—Å—Ç–∞–ª—å–Ω—ã–µ —ç–ª–µ–º–µ–Ω—Ç—ã –≤—ã—á–∏—Å–ª—è—é—Ç—Å—è –∫–∞–∫ —Å—É–º–º–∞ –¥–≤—É—Ö —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –∏–∑ –ø—Ä–µ–¥—ã–¥—É—â–µ–π —Å—Ç—Ä–æ–∫–∏
            row.append(triangle[i-1][j-1] + triangle[i-1][j])
    
    triangle.append(row)

# –í—ã–≤–æ–¥–∏–º —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫
for row in triangle:
    print(' '.join(map(str, row)))
# Шаг 1: заполняем эту строку вправо — всё, строка готова.
# Шаг 2: «вниз» по столбцу — уже нет строк ниже, но границы могут быть такими, что цикл по r не выполнится; при этом right уменьшится.
# Шаг 3: без проверки мы бы снова прошли ту же строку влево и перезаписали бы её.
# Проверка top <= bottom не даёт выполнять шаг 3, когда «нижней» строки уже нет (она совпала с верхней и была заполнена на шаге 1).

# Аналогично: при одном столбце шаг 2 заполняет его вниз, а шаг 4 без проверки left <= right мог бы пройти тот же столбец вверх. Проверка это предотвращает.
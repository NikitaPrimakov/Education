# 1. –ü—Ä–æ–≤–µ—Ä–∏–º, —á—Ç–æ –≤ –º–∞—Ç—Ä–∏—Ü–µ –≤—Å–µ —á–∏—Å–ª–∞ –æ—Ç 1 –¥–æ n¬≤

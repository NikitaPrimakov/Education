# –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ —Ä–µ–∑—É–ª—å—Ç–∞—Ç –Ω–∞ —ç–∫—Ä–∞–Ω, —á–∏—Å–ª–∞ –¥–æ–ª–∂–Ω—ã –±—ã—Ç—å —Ä–∞–∑–¥–µ–ª–µ–Ω—ã –æ–¥–Ω–∏–º –ø—Ä–æ–±–µ–ª–æ–º.


n = int(input('Enter size of matrix: '))

matrix = list()
matrix_reverse = list()

for i in range(n):
    row = input().split(' ')
    for j in range(len(row)):
        row[j] = int(row[j])
    matrix.append(row)

for i in range(1, n+1):
    list_for_matrix = matrix[-i]
    matrix_reverse.append(list_for_matrix)

for c in range(n):
    for r in range(n):
        print(matrix_reverse[r][c], end=' ')
    print()



# Индекс массы тела

# Напишите программу для вычисления и оценки индекса массы тела (ИМТ) человека. ИМТ показывает, человек весит больше или меньше нормы для своего роста. 
# ИМТ человека рассчитывают по формуле:

# ИМТ = масса (кг) / рост (м) * рост (м)

weight = float(input("Введите вес: "))
height_man = float(input("Введите рост: "))

index = weight / (height_man * height_man)

if 18.5 <= index <= 25:
    print("Оптимальная масса")
elif index < 18.5:
    print("Недостаточная масса")
else:
    print("Избыточная масса")


# Зодиак

# Китайский гороскоп назначает животным годы в 12-летнем цикле. Один 12-летний цикл показан в таблице ниже. Таким образом, 2012 год будет очередным годом дракона.

# Список животных китайского зодиака в порядке цикла
zodiac_animals = ["Обезьяна", "Петух", "Собака", "Свинья", 
                  "Крыса", "Бык", "Тигр", "Заяц", 
                  "Дракон", "Змея", "Лошадь", "Овца"]

# Ввод года от пользователя
year = int(input("Введите год: "))

# Определение животного по году
animal = zodiac_animals[year % 12]

# Вывод результата
print(f"Год {year} - год {animal} по китайскому гороскопу.")

# Как это работает:

# Животные в китайском зодиаке следуют в определённом порядке с циклом в 12 лет.

# 2012 год был годом Дракона, что соответствует индексу 8 в списке (поскольку 2012 % 12 = 8).

# Для любого введённого года мы вычисляем остаток от деления на 12 (year % 12), который и будет индексом животного в списке.
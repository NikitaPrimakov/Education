# –Ω—É–º–µ—Ä–∞—Ü–∏—è –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è —Å –µ–¥–∏–Ω–∏—Ü—ã.

num = int(input("–í–≤–µ–¥–∏—Ç–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Ö–æ–ª–æ–¥–∏–ª—å–Ω–∏–∫–æ–≤: "))
infected = []

for i in range(1, num + 1):
    s = input(f"–í–≤–µ–¥–∏—Ç–µ —Å—Ç—Ä–æ–∫—É —Ö–æ–ª–æ–¥–∏–ª—å–Ω–∏–∫–∞ ‚Ññ {i}: ")
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç–∏ "anton"
    target = "anton"
    index = 0
    
